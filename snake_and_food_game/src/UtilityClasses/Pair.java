package snake_and_food_game.src.UtilityClasses;

public class Pair {
    public final int row;
    public final int col;

    public Pair(int row, int col) {
        this.row = row;
        this.col = col;
    }

    public int getRow() {
        return row;
    }

    public int getCol() {
        return col;
    }
}
